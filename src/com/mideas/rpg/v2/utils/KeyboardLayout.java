package com.mideas.rpg.v2.utils;

public class KeyboardLayout {

	public static String getKey(int id) {
		if(id == 1) {
			return "Escape";
		}
		if(id == 2) {
			return "1";
		}
		if(id == 3) {
			return "2";
		}
		if(id == 4) {
			return "3";
		}
		if(id == 5) {
			return "4";
		}
		if(id == 6) {
			return "5";
		}
		if(id == 7) {
			return "6";
		}
		if(id == 8) {
			return "7";
		}
		if(id == 9) {
			return "8";
		}
		if(id == 10) {
			return "9";
		}
		if(id == 11) {
			return "0";
		}
		return null;
	}
}
